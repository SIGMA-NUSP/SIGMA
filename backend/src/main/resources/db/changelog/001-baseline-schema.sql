-- Schema: NUSP (usuário NUSP no PDB XEPDB1)
-- Migrado de: PostgreSQL 16 (schemas: cadastro, forms, operacao, pessoa)
-- ============================================================
--
-- CONVENÇÕES:
--   CAD_  → schema PostgreSQL 'cadastro'
--   FRM_  → schema PostgreSQL 'forms'
--   OPR_  → schema PostgreSQL 'operacao'
--   PES_  → schema PostgreSQL 'pessoa'
--
--   Booleans : NUMBER(1) — 0=false, 1=true
--   UUIDs    : VARCHAR2(36) — formato 'xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx'
--   Horários : VARCHAR2(8) — formato 'HH:MM:SS' (tipo time do PG)
--   JSON     : CLOB (tipo jsonb do PG)
--   IDs auto : GENERATED BY DEFAULT ON NULL AS IDENTITY
--
-- LÓGICA DE NEGÓCIO REMOVIDA DO BANCO (migrada para Java):
--   - Triggers set_updated_at    → JPA @PreUpdate  (AuditableEntity)
--   - Trigger sync_houve_anom    → AnormalidadeService.syncHouveAnormalidade()
--   - CHECK constraints de enum  → Java enums (@Enumerated)
--   - CHECK constraints condic.  → validações nos Services
--   - DEFAULT now()              → JPA @PrePersist / @PreUpdate
--   - DEFAULT gen_random_uuid()  → JPA @GeneratedValue(UUID)
--   - Defaults de boolean/enum   → inicialização de campo Java
--   - Extensão citext            → toLowerCase() nos setters
-- ============================================================

