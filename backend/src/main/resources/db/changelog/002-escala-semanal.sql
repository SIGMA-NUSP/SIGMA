-- ============================================================
-- OPR_ESCALA_SEMANAL — Escala semanal de operadores
-- ============================================================
CREATE TABLE OPR_ESCALA_SEMANAL (
    ID             NUMBER(10)     GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    DATA_INICIO    DATE           NOT NULL,
    DATA_FIM       DATE           NOT NULL,
    CRIADO_POR     VARCHAR2(100),
    CRIADO_EM      TIMESTAMP      NOT NULL,
    ATUALIZADO_EM  TIMESTAMP      NOT NULL
);

-- ============================================================
-- OPR_ESCALA_OPERADOR — Vínculo operador ↔ sala na escala
-- ============================================================
CREATE TABLE OPR_ESCALA_OPERADOR (
    ID             NUMBER(10)     GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ESCALA_ID      NUMBER(10)     NOT NULL,
    SALA_ID        NUMBER(5)      NOT NULL,
    OPERADOR_ID    VARCHAR2(36)   NOT NULL,
    CONSTRAINT FK_ESCALA_OP_ESCALA  FOREIGN KEY (ESCALA_ID)   REFERENCES OPR_ESCALA_SEMANAL(ID) ON DELETE CASCADE,
    CONSTRAINT FK_ESCALA_OP_SALA    FOREIGN KEY (SALA_ID)     REFERENCES CAD_SALA(ID),
    CONSTRAINT FK_ESCALA_OP_OPERADOR FOREIGN KEY (OPERADOR_ID) REFERENCES PES_OPERADOR(ID)
);

CREATE INDEX IDX_ESCALA_OP_ESCALA ON OPR_ESCALA_OPERADOR (ESCALA_ID);
CREATE INDEX IDX_ESCALA_OP_SALA ON OPR_ESCALA_OPERADOR (SALA_ID);
CREATE INDEX IDX_ESCALA_OP_OPERADOR ON OPR_ESCALA_OPERADOR (OPERADOR_ID);
