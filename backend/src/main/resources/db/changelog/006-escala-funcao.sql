-- ============================================================
-- OPR_ESCALA_FUNCAO — Vínculos de função especial por escala
-- (Apoio às Comissões, Fechamento dos Plenários, etc.)
-- ============================================================
CREATE TABLE OPR_ESCALA_FUNCAO (
    ID             NUMBER(10)     GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ESCALA_ID      NUMBER(10)     NOT NULL,
    TIPO           VARCHAR2(40)   NOT NULL,
    OPERADOR_ID    VARCHAR2(36)   NOT NULL,
    CONSTRAINT FK_ESCALA_FUNC_ESCALA   FOREIGN KEY (ESCALA_ID)   REFERENCES OPR_ESCALA_SEMANAL(ID) ON DELETE CASCADE,
    CONSTRAINT FK_ESCALA_FUNC_OPERADOR FOREIGN KEY (OPERADOR_ID) REFERENCES PES_OPERADOR(ID)
);

CREATE INDEX IDX_ESCALA_FUNC_ESCALA   ON OPR_ESCALA_FUNCAO (ESCALA_ID);
CREATE INDEX IDX_ESCALA_FUNC_OPERADOR ON OPR_ESCALA_FUNCAO (OPERADOR_ID);
CREATE INDEX IDX_ESCALA_FUNC_TIPO     ON OPR_ESCALA_FUNCAO (ESCALA_ID, TIPO);
